const lines = input.split('\n');

const points = {
  ')': 3,
  ']': 57,
  '}': 1197,
  '>': 25137
};

const open = {
  '(': true,
  '[': true,
  '{': true,
  '<': true,
  ')': false,
  ']': false,
  '}': false,
  '>': false
};

const closer = {
  '(': ')',
  '[': ']',
  '{': '}',
  '<': '>'
}

const incomplete = [];
const score = lines.reduce((acc, line) => {
  const stack = [];
  let index = -1;

  for (let i = 0; i < line.length; i++) {
    if (open[line[i]]) {
      stack.push(closer[line[i]]);
      index++;
    } else {
      if (line[i] === stack[index]) {
        stack.pop();
        index--;
      } else {
        return acc + points[line[i]];
      }
    }
  }

  incomplete.push(stack);
  return acc;
}, 0);

console.log(`Part 1: ${score}`);

const p2points = {
  ')': 1,
  ']': 2,
  '}': 3,
  '>': 4
}
const p2scores = incomplete.map(stack => {
  let acc = 0;
  for (let i = stack.length-1; i >= 0; i--) {
    acc = (acc * 5) + p2points[stack[i]];
  }
  return acc;
}, 0);

const sorted = p2scores.sort((a,b) => a > b ? 1:-1);

console.log(`Part 2: ${sorted[(sorted.length-1)/2]}`);
